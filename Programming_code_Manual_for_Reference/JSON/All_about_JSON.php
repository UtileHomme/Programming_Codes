<!-- What is JSON -->

<!-- Reference - https://www.youtube.com/watch?v=-D3dmMfQ9p0 -->

- Javascript Object Notation
- It is used for talking between two applications in the form of API
- it is a lightweight data-interchange format

<!-- Different datatypes that can be used with JSON -->

1. Number
- No difference between integer and floats

2. String
- String of Unicode characters
- use double quotes

3. Boolean
- true or false

4. Arrays
- Ordered list of values

5. Object
- Unordered collection of key/value pairs

6. Null
- Empty values

<!-- JSON Syntax Rules -->

- uses key/value pairs - {"name": "Brad"}

- file type is ".json"

- MIME Type is "Application/json"

