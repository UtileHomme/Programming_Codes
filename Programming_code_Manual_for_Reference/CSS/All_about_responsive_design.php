<!-- Why responsive design is important -->

- people view websites on many different devices
- make the site easy to use and easy on the eye

<!-- What is a viewport -->
- it is the size of the window for the device on which we are trying to view our website
- it is not necessary that the window and the size of the device ALWAYS match

<!-- What do media queries do -->
- allow us to create CSS rules based on various parameters(viewport size, device type)

<!-- How to define media queries -->

@media screen and (max-width: 800px)
{
    //if the screen is less than 800 px only then apply these rules
}
