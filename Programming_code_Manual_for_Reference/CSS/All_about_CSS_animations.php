<!-- What is the use of CSS animations -->

- Make animating web elements much easier without the need of Javascript/jQuery

<!-- What are CSS Transforms -->
- they transform the element by changing the orientation of the web element

<!-- Some attributes -->

transform: translateX(200px);
- moves the object 200px to the right from its current position

transform: translateY(200px);
- moves the object 200px to the bottom from its current position

transform: translate(200px,400px);
- moves the object both across the X and Y axis

transform: scaleX(2);
- increases/decreases the size of the object depending on the integer value
- Default is "1"
- rest of the attributes are same as above

transform: rotateZ(90deg)
- is for rotating an object around the mentioned "axis"
