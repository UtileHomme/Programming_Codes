<!-- What is a Rest API -->

<!-- Reference - https://www.youtube.com/watch?v=Q-BpqyOT3a8&t=927s -->

- stands for "Representational State Transfer"

- is an architectural style for designing networked applications

- relies on a stateless, client server protocol, almost always HTTP

- treats server objects (Eg - Blog Post) as resources that can be created or destroyed

- can be used virtually any programming language

