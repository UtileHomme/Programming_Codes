<!-- What is a Web Service and how are APIs related to them -->

<!-- Reference - https://www.youtube.com/watch?v=FOZtRzY5x8E -->

- are set of rules and technologies that enable two or more components on the web to talk to each other

** Rest APIs are specific APIs which follow the "Rest" rules

 <!-- How does HTTP relate to REST -->

- HTTP is an application layer protocol for sending and receiving messages over a network

- REST is a specification that dictates how distributed systems on the web should communicate
- it is a way to implement the HTTP communication

<!-- REST rules -->

Questions asked while defining REST rules

1. How can the client tell the service provider which operation it wants to perform? (Method information)

2. How can the client tell the service provider what data to operate on? (Scoping information)

Rules

1. Method information: The method information should be expressed in the HTTP verb

We use the HTTP verb according to the purpose. Don't use "GET" for delete operations

