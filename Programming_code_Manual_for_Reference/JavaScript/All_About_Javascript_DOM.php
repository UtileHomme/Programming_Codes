<!-- What is Document Object Model -->

<!-- Reference - https://www.youtube.com/watch?v=H63dVFDuJDM -->

- it is an application programming interface
- it is a structured representation of html document
- a tree of nodes/elements created by the browser

- JS can be used to read/write/manipulate to the DOM

https://imgur.com/a/rflbMjc

- we use the DOM when we interact with web pages

a. Add content to a HTML document
b. Delete content from a HTML document
c. Change content on a HTML document

<!-- What each term means in DOM -->

1. Document
- The web page design

2. Object
- All elements (head, body, ul) are an object

3.  Model
- How these elements/objects are laid out in a tree fashion

 https://imgur.com/a/xwA7xAc

 ** Nodes
 - everything we can change in the documents i.e. Elements and the text within elements ; HTML attributes (href, value)

 <!-- What can we do with the DOM -->

Eg-
 - Change the <h1> text node at the top of the page
 - Change the background colour of an element node
 - Animate the logo node from left to the right
