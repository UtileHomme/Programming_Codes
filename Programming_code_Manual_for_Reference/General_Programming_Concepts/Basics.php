<!-- What is Dynamic and Static Typing -->

- the type of data an object can store is mutable
- any target may hold a binding to any kind of object

Eg - int i = 5;
        i = "5"

- this is allowed for "dynamic" typing but not in "static typing"

<!-- What is Strong and Weak typing -->

- For Strong typing, once assigned a value of a particular kind, objects obey strict rules about how they can interact with other objects
- Weak typing means such rules are much relaxed

Eg - 1 + "1"    //allowed in case of weak typing but not strong typing

** PHP is weakly and dynamicallly typed
