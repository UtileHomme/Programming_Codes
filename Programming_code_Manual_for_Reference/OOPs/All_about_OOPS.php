<!-- How is OOPs different from Functional Programming -->

- Object Oriented Programming is good when we have a fixed set of operations on things
and as the code evolves, we add new things.
- This can be accomplished by adding new classes which implement existing methods and the existing classes
are left alone

- Functional Languages are good when you have a fixed set of things and as the code evolves
we need to add "new" operations on existing things
- This can be accomplished by adding new functions which compute with existing data types and existing
functions are left alone
