<!-- What are Distributed systems -->

<!-- Reference - https://www.youtube.com/watch?v=rYK-kTBUrK4 -->

- If we want to build a system which can handle all kinds of requests, it becomes really difficult to have it all on one machine

- So , we distribute the system into smaller interconnected systems and they handle different parts of the load

- Even if, one of the systems stops working so the other machines handle that systems workload
