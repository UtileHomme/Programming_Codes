<!-- Why do we need PHP frameworks -->

- Frameworks abstract us from the low level details, makes us more productive, and protects us from low level errors (such as preventing SQL injection)
- it adds structure to the code, prompting the developer to write better, more readable and maintainable code
- the main / repetitive functionalities are already implemented for us
- Makes code lightweight, sharp and secure

- It helps other developers collaborate because all developers working on the project would have knowledge of the various "helper" functions

<!-- What does footprint mean  -->
- It designates the size occupied by our application in computer RAM memory
