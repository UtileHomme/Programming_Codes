<!-- To open the preferences menu  -->

Ctrl+,

<!-- To open the various commands pallete  -->

Ctrl+Shift+P

<!-- Browse list of open files in the editor -->

Ctrl+B

<!-- Reload Atom -->

Ctrl+Alt+R

<!-- Change the syntax highlighting,i.e., select which time of programming language syntax to apply -->

Ctrl+Shift+L

<!-- Show available code snippets -->

Alt+Shift+S

<!-- Markdown preview -->

Ctrl+Shift+M

<!-- Toggle Developer Tools -->

Ctrl+Alt+I

<!-- To bookmark a particular line in the file -->

Ctrl+Shift+f2

<!-- How to clear all book marks -->

Alt+Shift+f2

<!-- To view all bookmarks -->

Ctrl + f2

<!-- To move through bookmarks -->

Press F2

<!-- To move back through bookmarks -->

Press Shift + F2

<!-- How to join the next line to the end of the line -->

Ctrl + J

<!-- How to Move the current line up or down -->

Ctrl + Up/Down

<!-- Uppercase the current word -->

Ctrl + K then Ctrl + U

<!-- Lowercase the current word -->

Ctrl + K then Ctrl + L

<!-- Delete to the beginning of the word -->

Ctrl + Backspace

<!-- Delete to the end of the word -->

Ctrl + Delete

<!-- Select all matching words with the current selected word -->

Alt + F3

<!-- Selecting all the text in the present bracket -->

Alt + Ctrl + ,

<!-- Close the current XML and HTML tag -->

Alt + Ctrl + .
